    private void setView() {
        FinancialReportRepo fr = new FinancialReportRepo();
        FinancialReportItemRepo frr = new FinancialReportItemRepo();
        
        try {
            toEdit = fr.getById(viewId);
            reportCodeField.setText(toEdit.getReportCode());
            descriptionField.setText(toEdit.getDescription());
            reportDateField.setText(String.valueOf(toEdit.getCreationDate()));
            
            jTable2.setModel(frr.getTableModel(viewId));
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(FinancialReportNew.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private boolean validateInputs() {
        // Validate Description
        String description = descriptionField.getText().trim();
        if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Description cannot be empty.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            descriptionField.requestFocus();
            return false;
        }

        return true;
    }

