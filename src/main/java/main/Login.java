    private void loginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnMouseClicked
        // TODO add your handling code here:
        String usernameEntry = usernameTextField.getText();
        String passwordEntry = passwordTextField.getText();
        
        if(Authenticate.login(usernameEntry, passwordEntry)){
            currentUser = UserSession.getInstance().getCurrentUser();
            
            // Open the dashboard based on user role
            assignRoleDashboard(currentUser.getRole());
        }
    }//GEN-LAST:event_loginBtnMouseClicked

